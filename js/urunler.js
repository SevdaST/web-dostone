      function allvisiblefalse() //makes all cards  unvisible 
      {
        var urun=[];

        for (var i = 1; i <= 15; i++) {
          urun[i]="urun"+(i);
        }

        for (var i = 1; i <= 15; i++) {
          document.getElementById(urun[i]).style.display = "none";
          ////document.getElementById(urun[i]).style="visibility:hidden";
        } 
      }